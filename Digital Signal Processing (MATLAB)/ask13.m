syms z n
n = 0:8;

% Για το X(z)
xZ = z/(z-1/3);      % Μετασχηματισμός Ζ
xF = iztrans(xZ);    % Εύρεση αρχικής ακολουθίας
xFsol = subs(xF, n); % Υπολογισμός όρων

% Για το Y(z)
yZ = (z^2 + 1.6*z)/( (z+1.8)*(0.5*z-0.8) ); % Μετασχηματισμός Ζ
yF = iztrans(yZ);    % Εύρεση αρχικής ακολουθίας
yFsol = subs(yF, n); % Υπολογισμός όρων

% Γραφικές παραστάσεις
subplot(2, 1, 1);
stem(n, xFsol);
title('Ακολουθία Χ');
subplot(2, 1, 2);
stem(n, yFsol);
title('Ακολουθία Υ');
